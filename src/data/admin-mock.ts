export function getKennelOccupancy(kennelId: number, pets = mockAdminPets) {
  return pets.filter((p) => p.kennel_id === kennelId).length;
}

export function getStaffName(userId: number | null | undefined) {
  if (userId == null) return "—";
  return mockStaff.find((s) => s.user_id === userId)?.full_name ?? mockUsers.find((u) => u.user_id === userId)?.full_name ?? "—";
}

